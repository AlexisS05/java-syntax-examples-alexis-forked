package org.example;

public class Main {
    public static void main(String[] args) {

        System.out.println("Some if statements");

        int value = 17;

        // See Java Language Specification section 14.9 - The if Statement

        // "if-then" statement - 1 condition, 0 else

        if (value == 17) System.out.println("Value is 17");

        // "if-then-else" statement - 1 condition, 1 else

        if (value == 17) System.out.println("Value is 17");
        else System.out.println("Value is NOT 17");

        // "if-then-else



    }
}